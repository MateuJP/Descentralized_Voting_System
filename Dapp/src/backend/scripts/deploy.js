const { ethers } = require("hardhat");

async function main() {
  const [deployer] = await ethers.getSigners();

  console.log("Deploying contracts with the account:", deployer.address);
  console.log("Account balance:", (await deployer.getBalance()).toString());

  const Token=await ethers.getContractFactory("MyToken");
  const token = await Token.deploy("100000000000000000");
  const Polling= await ethers.getContractFactory("PollingStation");
  const polling= await Polling.deploy(token.address);
  await token.addSub(polling.address);
  
  // Save copies of each contracts abi and address to the frontend.
  saveFrontendFiles(token, "MyToken");
  saveFrontendFiles(polling,"PollingStation");
}

